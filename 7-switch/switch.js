//switch case
const nilai = 'A'

switch (nilai) {
    case 'A':
        console.log('sangat baik')
        break //digunakan untuk menghentikan program ketika kondisinya sudah terpenuhi
    case 'B':
        console.log('baik')
        break
    case 'C':
        console.log('cukup')
        break
    default:
        console.log('tidak lulus')
}